## Data Flow

### Authentication Flow

```
1. User submits login form
2. Frontend calls authService.login()
3. Axios interceptor adds token to request
4. Backend validates credentials
5. Backend returns JWT token
6. Frontend stores token (localStorage/SecureStore)
7. Context updates with user data
8. User redirected to dashboard
```

### Learning Flow

```
1. User navigates to learning center
2. Frontend fetches courses via courseService
3. User selects course
4. Frontend fetches lessons
5. User watches video lesson
6. User completes quiz
7. Frontend submits quiz answers
8. Backend validates and scores
9. Progress updated in context
10. Certificate issued on completion
```

